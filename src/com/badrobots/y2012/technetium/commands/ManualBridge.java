package com.badrobots.y2012.technetium.commands;

import com.badrobots.y2012.technetium.OI;

/**
 *
 * @author Jon Buckley
 */
public class ManualBridge extends CommandBase
{
    
    public ManualBridge()
    {
        requires(bridgeTool);
    }

    // Called just before this Command runs the first time
    protected void initialize()
    {
        System.out.println("initializing ManualBridge");
    }

    // Called repeatedly when this Command is scheduled to run
    protected void execute()
    {
        double speed = 0;
        System.out.println("executing from ManualBridge");
        
        if (OI.primaryXboxLeftJoyClick())
        {
            System.out.println("y depressed");
            speed = 1;
        }
        else if (OI.primaryXboxRightJoyClick())
            speed = -1;   
        bridgeTool.setMotor(speed);
    }

    // Make this return true when this Command no longer needs to run execute()
    protected boolean isFinished()
    {
        return false;
    }

    // Called once after isFinished returns true
    protected void end()
    {
    }

    // Called when another command which requires one or more of the same
    // subsystems is scheduled to run
    protected void interrupted()
    {
    }
}
